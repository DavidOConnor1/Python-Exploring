# multiple assignments = allows us to assign various amount of variables at the same time with one line of code

#example of without multiple assignments
name = "Bro"
age = 25
attractive = True

print(name)
print(age)
print(attractive)

#example of with multiple assignments

name1, age1, attractive1 = "Kurt", 27, True

print(name1)
